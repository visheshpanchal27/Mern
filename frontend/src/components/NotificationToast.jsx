import { toast } from 'react-toastify';

// Enhanced toast notifications with better styling
export const showSuccessToast = (message) => {
  toast.success(message, {
    position: "top-right",
    autoClose: 3000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    className: 'bg-green-500 text-white',
  });
};

